import math
#floor rounds down
from math import floor
#ceil rounds up
from math import ceil
num1 = 5.6
num2 = 4.5329211
num3 = [12.5, 4.5, 6.7, 9.8]
print(ceil(num1))
print(floor(num1))
#rounds up to 2 decimal places
print(round(num2, 3))
#takes an iterable list and adds them up
print(math.fsum(num3))
#exponents (pow) * [a number to the power of another number]
#print(2**6) or
print(math.pow(2,2))

